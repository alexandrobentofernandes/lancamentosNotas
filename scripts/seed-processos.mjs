function parseTSV(text) {
  const lines = text.trim().split('\n');
  const headers = lines[0].split('\t');
  return lines.slice(1).map(line => {
    const cols = line.split('\t');
    const obj = {};
    headers.forEach((h, i) => {
      let key = '';
      switch(h.trim()) {
        case 'PROCESSO': key = 'processo'; break;
        case 'CARGOS_PROCESSOS': key = 'cargo'; break;
        case 'ATIVIDADE DO PROCESSO': key = 'atividade'; break;
        case 'VALOR BAREMO DO PROCESSO': key = 'valorBaremo'; break;
        case 'IT DO PROCESSO': key = 'it'; break;
        case 'ÚNICOS': key = 'unicos'; break;
        default: key = h.trim().toLowerCase().replace(/\s+/g, '_');
      }
      obj[key] = (cols[i] || '').trim().replace(/\s+$/, '');
    });
    return obj;
  });
}

