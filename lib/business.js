export function calcStatusProvaOnline(v) {
  if (!v) return '';
  return v === 'NÃO REALIZADO' ? 'NOK' : 'OK';
}
export function calcStatusProvaTeorica(nota) {
  if (nota === null || nota === undefined || nota === '') return '';
  const n = parseFloat(nota);
  if (isNaN(n)) return '';
  if (n === 0) return 'NÃO REALIZADO';
  return n > 6.9 ? 'APROVADO' : 'NÃO APTO';
}
export function calcStatusParcial(f) {
  const st = [f.statusProva1,f.statusProva2,f.statusProva3,f.statusProva4,f.statusProva5,f.statusProva6].filter(s => s && s !== '');
  if (!st.length) return '';
  const na = st.some(s => s === 'NÃO APTO');
  const tf = f.statusProvaTeorica === 'NÃO APTO';
  if (!na && !tf) return 'APROVADO';
  if (na && tf) return 'REPROVADO NA PRÁTICA + TEORIA';
  if (na) return 'REPROVADO NA PRÁTICA';
  if (tf) return 'SEM REPASSE';
  return 'APROVADO';
}
export function calcResultadoFinal(f) {
  if (f.pedido === 'NO SHOW') return 'AUSENTE';
  const sp = f.statusParcial;
  if (!sp) return 'PENDENTE';
  if (sp === 'APROVADO') return 'APROVADO';
  if (sp.includes('REPROVADO') || sp === 'SEM REPASSE') return 'REPROVADO';
  return 'PENDENTE';
}
export function calcISBN(cpf) {
  if (!cpf) return '';
  const c = cpf.replace(/\D/g, '');
  return c ? '721449704000116' + c : '';
}
export function getBaremo(processo, atividade) {
  const p = PROCESSOS_MAP[processo];
  if (!p) return '';
  const a = p.atividades.find(x => x.a === atividade);
  return a ? a.b : '';
}
export function getIT(processo) {
  const p = PROCESSOS_MAP[processo];
  return p && p.atividades[0] ? p.atividades[0].it : '';
}
export function getCargo(processo) {
  return PROCESSOS_MAP[processo]?.cargo || '';
}
export function getEmpresaData(nome) {
  return EMPRESAS.find(e => e.nome === nome) || null;
}

export function applyBusinessRules(form) {
  const f = { ...form };
  if (f.empresa) {
    const ed = getEmpresaData(f.empresa);
    if (ed) {
      f.emailEmpresa = ed.email;
      f.dataInicioAssinatura = ed.dataInicio;
      f.dataFimAssinatura = ed.dataFim;
      f.associadoSindistal = ed.associado ? 'SIM' : 'NÃO';
      f.docContratual = ed.docContratual;
      f.nRenovacao = String(ed.valor);
    }
    f.estado = 'RJ';
  }
  if (f.base) {
    const m = BASE_UO_REGIAO[f.base];
    if (m) { f.unidadeOperacional = m.UO; f.regiao = m.REGIAO; }
  }
  if (f.processo) {
    f.cargosProcessos = getCargo(f.processo);
    const it = getIT(f.processo);
    ['1','2','3','4','5','6'].forEach(n => { f['it' + n] = it; });
  }
  ['1','2','3','4','5','6'].forEach(n => {
    if (f['avaliacao' + n]) f['baremo' + n] = getBaremo(f.processo, f['avaliacao' + n]);
  });
  f.statusProvaOnline = calcStatusProvaOnline(f.avaliacaoOnline);
  f.statusProvaTeorica = calcStatusProvaTeorica(f.notaProva);
  if (f.cpf) f.isbn = calcISBN(f.cpf);
  f.statusParcial = calcStatusParcial(f);
  f.resultadoFinal = calcResultadoFinal(f);
  return f;
}
